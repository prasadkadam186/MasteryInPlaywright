// Traditional for Loop 
console.log("******** Traditional For Loop ***************");

for(let i=0;i<5;i++)
{
    console.log("Hi");
}
// While loop
console.log("******** While Loop ***************");
let attempt=0;
while(attempt < 3){
    console.log(`${attempt}`)
    attempt++;
}
// Do While
console.log("******** do While Loop ***************");
let num=10
do{
    console.log(num);
    num--;
} while (num>=0);